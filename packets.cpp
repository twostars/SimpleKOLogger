#define CASE_OP(opcode) case opcode: return #opcode

const char* login_opcode_to_name(uint8_t opcode)
{
	switch (opcode)
	{
	CASE_OP(LS_VERSION_REQ);
	CASE_OP(LS_DOWNLOADINFO_REQ);
	CASE_OP(LS_LOGIN_REQ);
	CASE_OP(LS_MGAME_LOGIN);
	CASE_OP(LS_SERVERLIST);
	CASE_OP(LS_NEWS);

	default:
		return "";
	}
}

